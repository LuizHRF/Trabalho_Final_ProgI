    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Tarefa tarefa = new Tarefa();
        
        if(edtTarefa.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Tarefa n√£o preenchida");
            return;
        }
        
        tarefa.setTarefa(edtTarefa.getText());
        
        try{
            RestTemplate restTemplate = new RestTemplate();
            restTemplate.postForObject("localhost:8080/ListaTarefas", tarefa, Tarefa.class);
            JOptionPane.showMessageDialog(this, "Tarefa salva com sucesso!");
        }catch(RestClientException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        loadTableData();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void loadTableData(){
        RestTemplate restTemplate = new RestTemplate();
        
        ResponseEntity<Tarefa[]> response = restTemplate.getForEntity("localhost:8080/ListaTarefas", Tarefa[].class);
        
        
        Tarefa[] tarefas = response.getBody();
        
        DefaultTableModel tableModel = (DefaultTableModel) tblTarefas.getModel();
        
        tableModel.setNumRows(0);
        
        for(Tarefa tarefa : tarefas){
            Object[] row = {tarefa.getId(), tarefa.getTarefa()};
            tableModel.addRow(row);
        }
    }
